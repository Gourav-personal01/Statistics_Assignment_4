# Q9: What is Central Limit Theorem? State the significance of the Central Limit Theorem.

# Answer - 

# Central Limit theorem states that the sampling distribution of the Mean is always normal distributed whether the distribution is 
# poisson, binomial, uniform or bernaulli's. 

# X = x1 + x2 + x3 + - - - - - - Xn

# Where X is the Mean of all sample mean and x1, x2 are the mean of sample data


# Significance - For a large n, it says the sampling distribution of the sample mean is approximately normal, regardless of the distribution of the population.